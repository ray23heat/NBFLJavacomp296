package sportFans;



public class NBATeam {

	String name;
	String coach;
	
	
  public NBATeam (String name, String coach) {
	  this.name = name;
	  this.coach = coach;
	
	  
  }
  public String getName() {
	  return this.name;
	  
  }
  public String getCoach() {
	  
	  return this.coach;
  }

}
  
	 

